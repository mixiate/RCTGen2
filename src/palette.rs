pub struct NearestColour {
    pub index: u8,
    pub _error: glam::Vec3,
}

pub fn get_nearest_colour(colour: &glam::Vec3) -> NearestColour {
    let mut index = 0;
    let mut minimum_error = f32::INFINITY;

    for range in PALETTE_RANGES {
        for i in range {
            let error = colour - PALETTE_LINEAR[i];
            let error = error.dot(error).sqrt();

            if error < minimum_error {
                index = i;
                minimum_error = error;
            }
        }
    }

    NearestColour {
        index: index.try_into().unwrap(),
        _error: colour - PALETTE_LINEAR[index],
    }
}

